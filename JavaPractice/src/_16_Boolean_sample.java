
public class _16_Boolean_sample {
	
	public static void main(String[] args) {	
		
		System.out.println("1 == 1 " + (1==1));
		System.out.println("1 == 2 " + (1==2));
		System.out.println("1 != 2 " + (1!=2));
		
		int a = 1;
		int b = 2;
		
		System.out.println("a == a " + (a==a));
		System.out.println("a == b " + (a==b));
		System.out.println("a != b " + (a!=b));
		
	}

}
